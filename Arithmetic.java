class Main{
    public static void main(String[]args){
        int a = 60;
        int b = 2;
        int sum,sub,multi,divi,modulo;
        sum = a+b;
        sub = a-b;
        multi = a*b;
        divi = a/b;
        modulo = a%b;
        System.out.println("The addition of two no is : "+(sum));
        System.out.println("The subtraction of two no is : "+(sub));
        System.out.println("The multiplication of two no is : "+(multi));
        System.out.println("The division of two no is : "+(divi));
        System.out.println("The modulo of two no is : "+(modulo));

    }
}
